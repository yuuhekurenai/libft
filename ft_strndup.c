#include <stddef.h>
#include <stdlib.h>

#include "libft.h"

char	*ft_strndup(const char *s)
{
	void	*ptr;
	size_t	i;

	i = ft_strlen(s);
	ptr = malloc(i + 1);
	if (!ptr)
		return (NULL);
	ft_memcpy(ptr, s, i);
	return (ptr);
}
